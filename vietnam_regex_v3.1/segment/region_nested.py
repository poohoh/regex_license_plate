import json
from collections import defaultdict

def save_json(file_path, data):
    with open(file_path, "w", encoding="utf-8") as json_file:
        json.dump(data, json_file, ensure_ascii=False, indent=4)



# trie 형식으로 저장
def build_nested_trie():
    def nested_dict():
        return defaultdict(nested_dict)
    
    # trie 구조로 모든 키에 해당하는 데이터를 저장
    def insert_into_trie(trie, key, value):
        current = trie
        for char in key[:-1]:
            if char not in current:
                current[char] = {}
            current = current[char]
        current[key[-1]] = value
    
    trie_structure = nested_dict()

    trie_structure["region"] = {}
    trie_regions = trie_structure["region"]  # trie structure의 regions 부분

    for pattern, value in region.items():
        clean_pattern = pattern.strip('^$')
        
        insert_into_trie(trie_regions, clean_pattern, value)
    
    return trie_structure

