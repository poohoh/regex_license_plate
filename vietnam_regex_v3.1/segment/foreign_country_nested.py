# trie 형식으로 저장
def build_nested_trie():
    def nested_dict():
        return defaultdict(nested_dict)
    
    # trie 구조로 모든 키에 해당하는 데이터를 저장
    def insert_into_trie(trie, key, value):
        current = trie
        for char in key[:-1]:
            if char not in current:
                current[char] = {}
            current = current[char]
        current[key[-1]] = value
    
    trie_structure = nested_dict()

